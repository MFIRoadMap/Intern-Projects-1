    public void IletisimListele(ArrayList<SiparisBeans> List) {

        DefaultTableModel model = (DefaultTableModel) SiparisHazirla_Table.getModel();
        model.setRowCount(0);

        Object[] row = new Object[6];
        //kolon sayısını belirledim

        for (int i = 0; i < List.size(); i++) {
            row[0] = List.get(i).getSHT_Id();
            row[1] = List.get(i).getSHT_MusteriId();
            row[2] = List.get(i).getSHT_Siparis_Tarih();
            row[3] = List.get(i).getSHT_AlimTime();
            row[4] = List.get(i).getSFT_FisId();
            row[5] = List.get(i).getSHT_Durum();
            model.addRow(row);
        }
    }

