    public void SipariSorgu() {
        SiparisList.clear();
        SiparisBeans sb;
        String query, SHT_Id, SHT_MusteriId, SHT_AlimTime, SHT_Durum, SHT_Siparis_Tarih, SFT_FisId;

        try {
            Statement st = con.createStatement();
            query = "select * from Siparis_Hazirlat_Table ";
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
                SHT_Id = rs.getString("SHT_Id");
                SHT_MusteriId = rs.getString("SHT_MusteriId");
                SHT_AlimTime = rs.getString("SHT_AlimTime");
                SHT_Durum = rs.getString("SHT_Durum");
                SHT_Siparis_Tarih = rs.getString("SHT_Siparis_Tarih");
                SFT_FisId = rs.getString("SFT_FisId");

                sb = new SiparisBeans(SHT_Id, SHT_MusteriId, SHT_AlimTime, SHT_Durum, SHT_Siparis_Tarih, SFT_FisId);
                SiparisList.add(sb);
            }
            IletisimListele(SiparisList);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Bu Id değerine sahip müşteri bilgisi bulunamadı");
        }

    }

