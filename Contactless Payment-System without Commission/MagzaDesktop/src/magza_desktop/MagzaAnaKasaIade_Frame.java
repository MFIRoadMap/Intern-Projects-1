    public void UrunIade(String SatisId, String UrunBarkod) {
        String query = "UPDATE Satis_Table SET Satis_Durum = 'I' WHERE Satis_Id = ?";
        boolean durum = true;
        double iadebakiye ;
        try {
            PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1, SatisId);
            ps.executeUpdate();

            UrunStokGuncelle(UrunBarkod);
            UrunIadeTableOlustur(UrunBarkod);
            iadebakiye = BakiyeBul(UrunBarkod);
            IadeYuklenecekBakiye += iadebakiye;
            FisFiyatGuncelle(iadebakiye);
            IadeEdilecekBakiye_jLabel.setText(String.valueOf(IadeYuklenecekBakiye));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ürün iadesi yapılamadı");
        }
    }

    public void UrunIadeTableOlustur(String UrunBarkod) {
        String query = "INSERT INTO  Iade_Table (Iade_Urun_Barkod, Iade_Tarih, Iade_Fis_No, Iade_Musteri_Id, Iade_Kasa_Id)"
                + " VALUES (?, GETDATE(), ?, ?, ?)";
        try {
            PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1, UrunBarkod);
            ps.setString(2, FisNo);
            ps.setString(3, MusteriId);
            ps.setString(4, SecilenKasa);
            ps.executeUpdate();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ürün iadesi yapılamadı (iade tablosu oluşturulamadı)");
        }
    }

    public double BakiyeBul(String UrunBarcode) {
        String U_Fiyat, query;
        double fiyat = 0;
        try {
            Statement st = con.createStatement();
            query = "SELECT * FROM Urun_Table WHERE Urun_Barkod ='" + UrunBarcode + "'";
            ResultSet rs = st.executeQuery(query);

            while (rs.next()) {
                U_Fiyat = rs.getString("Urun_Fiyat");
                fiyat = Double.valueOf(U_Fiyat);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Fiyat bilgisi bulunamadı");
        }
        return fiyat;
    }

