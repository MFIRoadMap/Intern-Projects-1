    public void FisGetir() {
        KartUrunSatis_Beans kusb;
        String query, U_Barkod, U_Marka, U_Isim, U_Durum, U_Stok, U_Fiyat, Kart_Id, Kart_Bakiye, Satis_Id;
        byte[] UrunImage = null;
        //Öncelikle tabloyu temizliyorum ve tabloya yansıtıyorum yoksa yeni fiş girişlerini eski girişlerin üzerine ekliyor
        TableTemizle();
//BURADA HATA VAR//
        try {
            Statement st = con.createStatement();
            query = "select * from Satis_Fis_Table sft, Satis_Table st, Musteri_Table mt, Urun_Table ut  where \n"
                    + "sft.Satis_Fis_Id = st.Satis_Fis_Id and st.Satis_Durum = 'S' and st.Satis_Urun_Barkod = ut.Urun_Barkod and sft.Satis_Musteri_Id = mt.Musteri_Id \n"
                    + "and mt.Musteri_Id = " + MusteriId + " and sft.Satis_Fis_Id = " + FisNo;
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
                U_Barkod = rs.getString("Urun_Barkod");
                U_Marka = rs.getString("Urun_Marka");
                U_Isim = rs.getString("Urun_Isim");
                U_Stok = rs.getString("Urun_Stok");
                U_Durum = rs.getString("Urun_Durum");
                U_Fiyat = rs.getString("Urun_Fiyat");
                Kart_Id = rs.getString("Satis_Kart_Id");
                Satis_Id = rs.getString("Satis_Id");
                UrunImage = rs.getBytes("Urun_Gorsel");
                kusb = new KartUrunSatis_Beans(U_Barkod, U_Marka, U_Isim, U_Stok, U_Durum, U_Fiyat, Kart_Id, Satis_Id, UrunImage);
                barcodes.add(kusb);
                MusteriKartId = Kart_Id;
            }

            FisGoruntule();
            BakiyeGetir();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Yapılan girişler için bir kayıt bulunamadı");
        }
    }

