    public void IletisimIdSorgu() {

        TableTemizle();
        IletisimBeans ib;
        String query, Iletisim_Id, Iletisim_Persone_Id, Iletisim_Tur, Iletisim_Onem, Iletisim_Konu, Iletisim_Icerik,
                Iletisim_Gorsel, Iletisim_Tarih, Iletisim_Durum;
        try {
            Statement st = con.createStatement();
            query = "select * from Iletisim_Table";
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
                Iletisim_Id = rs.getString("Iletisim_Id");
                Iletisim_Persone_Id = rs.getString("Iletisim_Persone_Id");
                Iletisim_Tur = rs.getString("Iletisim_Tur");
                Iletisim_Onem = rs.getString("Iletisim_Onem");
                Iletisim_Konu = rs.getString("Iletisim_Konu");
                Iletisim_Icerik = rs.getString("Iletisim_Icerik");
                Iletisim_Gorsel = rs.getString("Iletisim_Gorsel");
                Iletisim_Tarih = rs.getString("Iletisim_Tarih");
                Iletisim_Durum = rs.getString("Iletisim_Durum");

                ib = new IletisimBeans(Iletisim_Id, Iletisim_Persone_Id, Iletisim_Tur, Iletisim_Onem, Iletisim_Konu,
                        Iletisim_Icerik, Iletisim_Gorsel, Iletisim_Tarih, Iletisim_Durum);
                IletisimList.add(ib);
            }
            IletisimListele();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Bu Id değerine sahip müşteri bilgisi bulunamadı");
        }

    }

    public void IletisimListele() {

        DefaultTableModel model = (DefaultTableModel) IletisimTable_Table.getModel();
        Object[] row = new Object[6];
        for (int i = 0; i < IletisimList.size(); i++) {
            row[0] = IletisimList.get(i).getIletisim_Id();
            row[1] = IletisimList.get(i).getIletisim_Persone_Id();
            row[2] = IletisimList.get(i).getIletisim_Konu();
            row[3] = IletisimList.get(i).getIletisim_Tur();
            row[4] = IletisimList.get(i).getIletisim_Durum();
            row[5] = IletisimList.get(i).getIletisim_Tarih();

            model.addRow(row);
        }
    }
