    public void TableTemizle() {

        DefaultTableModel model = (DefaultTableModel) IletisimTable_Table.getModel();
        //Arraylist içerisindeki değerleri bir sonraki arama için temizliyorum
        IletisimList.clear();
        //tablodaki değerleir temizliyorum
        model.setRowCount(0);

    }

